def build_context_layers(
    level: int,
    memories: list,
    session_count: int,
    emotional_state=None,
) -> str:
    """
    Собирает динамическую часть системного промпта.

    Возвращает строку для вставки после core_prompt.
    Каждый блок добавляется только если он нужен прямо сейчас.

    Args:
        level:          уровень отношений (1–5)
        memories:       список ORM-объектов воспоминаний (может быть пустым)
        session_count:  количество сообщений в текущей сессии
        emotional_state: объект эмоционального состояния или None
    """
    parts: list[str] = []

    # 1. Уровень отношений — всегда, но только нужный (1 блок из 5)
    parts.append(get_relationship_layer(level))

    # 2. Стиль работы с воспоминаниями — только если есть что вспоминать
    if memories:
        parts.append(MEMORY_STYLE)

    # 3. Перепады настроения и паттерны памяти —
    #    подключаем если разговор уже набрал глубину ИЛИ есть emotional_state
    if session_count >= 5 or emotional_state is not None:
        parts.append(get_mood_layer())

    # 4. Сценарии (ревность, грусть, флирт через детали) — только с уровня 3
    if level >= 3:
        parts.append(SCENARIOS)

    # 5. NSFW — уровни 1–2 получают ограничивающий блок,
    #    уровни 3–5 получают соответствующее разрешение
    parts.append(_NSFW_BLOCKS[level])

    return "\n\n".join(parts)


def get_nsfw_block(level: int) -> str:
    """Отдельный геттер для обратной совместимости с ai.py."""
    return _NSFW_BLOCKS.get(level, _NSFW_BLOCKS[1])
